# ---------------------------------------------------------------------------
# Artefact 3: Summary table (CSV)
# ---------------------------------------------------------------------------

def write_summary_table(
    rejection_rate: dict[tuple[str, int], float],
    ci: dict[tuple[str, int], tuple[float, float]],
    bad_accept_rate: dict[tuple[str, int], float],
    bad_accept_ci: dict[tuple[str, int], tuple[float, float]],
    ns: list[int],
) -> None:
    """Write CSV summary: per (strategy, n) rejection + bad-accept rate.

    Audit fix m1 (audit/soundness.md): the ``bad_accept_*`` columns
    capture the operationally-relevant Eq. (18) event
    ``Pr[accepted AND not hypothesis_correct]``.  This is the
    quantity the soundness theorem actually bounds; ``rejection_rate``
    can be misleading for adversaries that occasionally produce
    correct hypotheses by accident (e.g. ``random_list``).
    """
    path = OUT_DIR / "soundness_summary.csv"
    with open(path, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow([
            "strategy", "n",
            "rejection_rate", "ci_lower", "ci_upper",
            "bad_accept_rate", "bad_accept_ci_lower", "bad_accept_ci_upper",
        ])
        for strategy in STRATEGY_ORDER:
            for n in ns:
                rate = rejection_rate.get((strategy, n), 0)
                lo, hi = ci.get((strategy, n), (0, 0))
                bad = bad_accept_rate.get((strategy, n), 0)
                blo, bhi = bad_accept_ci.get((strategy, n), (0, 0))
                writer.writerow([
                    STRATEGY_LABELS[strategy],
                    n,
                    f"{rate:.4f}",
                    f"{lo:.4f}",
                    f"{hi:.4f}",
                    f"{bad:.4f}",
                    f"{blo:.4f}",
                    f"{bhi:.4f}",
                ])
    print(f"  {path.relative_to(PROJECT_ROOT)}")


# ---------------------------------------------------------------------------
# Artefact 3b: LaTeX table
# ---------------------------------------------------------------------------

def write_latex_table(
    rejection_rate: dict[tuple[str, int], float],
    ci: dict[tuple[str, int], tuple[float, float]],
    ns: list[int],
) -> None:
    """Write a LaTeX table: strategies as rows, select n as columns."""
    # Pick representative n values
    if len(ns) <= 8:
        ns_show = ns
    else:
        ns_show = ns[::3]
        if ns[-1] not in ns_show:
            ns_show.append(ns[-1])

    path = OUT_DIR / "soundness_summary.tex"
    with open(path, "w") as f:
        cols = "l" + "c" * len(ns_show)
        f.write(r"\begin{table}[htbp]" + "\n")
        f.write(r"\centering" + "\n")
        f.write(r"\caption{Soundness: rejection rate by adversarial strategy and $n$ (95\% Wilson CI).}" + "\n")
        f.write(r"\label{tab:soundness}" + "\n")
        f.write(r"\begin{tabular}{" + cols + "}\n")
        f.write(r"\toprule" + "\n")

        header = "Strategy & " + " & ".join(f"$n={n}$" for n in ns_show) + r" \\" + "\n"
        f.write(header)
        f.write(r"\midrule" + "\n")

        for strategy in STRATEGY_ORDER:
            cells = [STRATEGY_LABELS[strategy]]
            for n in ns_show:
                rate = rejection_rate.get((strategy, n), 0)
                lo, hi = ci.get((strategy, n), (0, 0))
                # Format as percentage
                if rate == 1.0:
                    cells.append("100\\%")
                else:
                    cells.append(f"{rate*100:.0f}\\% [{lo*100:.0f}, {hi*100:.0f}]")
            f.write(" & ".join(cells) + r" \\" + "\n")

        f.write(r"\bottomrule" + "\n")
        f.write(r"\end{tabular}" + "\n")
        f.write(r"\end{table}" + "\n")
    print(f"  {path.relative_to(PROJECT_ROOT)}")


