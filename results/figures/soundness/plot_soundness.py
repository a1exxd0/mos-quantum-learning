# The four-way mechanism decomposition used by the stacked-bar plots.  The
# ``accept`` category is split by whether the emitted hypothesis parity
# matches the true target, so readers can tell the operationally harmless
# "lucky correct accept" (random_list stumbles into the true parity) apart
# from a genuine Eq. (18) soundness violation.
MECHANISM_KEYS = (
    "reject_insufficient_weight",
    "reject_list_too_large",
    "accept_correct",
    "accept_wrong",
)


# ``experiments/harness/soundness.py:80`` hardcodes ``target_s = 1`` for
# every trial of the soundness experiment (the noiseless single-parity
# target against which every adversarial strategy is run).  The proto
# schema does *not* store ``target_s`` per trial, so the plot script
# hardcodes the same value here.
SOUNDNESS_TARGET_S = 1


def _is_correct(trial: dict) -> bool:
    """Decide whether an accepted trial produced the correct hypothesis.

    There are two sources of truth:

    * ``hypothesisCorrect`` — a bool set by the worker at
      ``worker.py:452`` as ``(hyp_s == target_s) if hyp_s is not None else False``.
      This field is the canonical signal going forward.
    * ``hypothesisS`` — the emitted parity index, present on every accepted
      trial.

    The ``results/soundness_4_20_100.pb`` artefact on disk at the time of
    this audit was generated by a worker build that did not correctly
    populate ``hypothesisCorrect`` for dishonest trials (the field came
    back as proto3 default ``False`` in every row, even on the
    ``random_list`` cells where the verifier visibly outputs
    ``hypothesisS = 1``).  To make this plot script robust across both
    pre-fix and post-fix ``.pb`` files we *derive* correctness from
    ``hypothesisS == target_s`` and cross-check against
    ``hypothesisCorrect`` when the latter is non-default.
    """
    hyp_s = trial.get("hypothesisS")
    if hyp_s is None:
        return False
    derived = (hyp_s == SOUNDNESS_TARGET_S)
    stored = trial.get("hypothesisCorrect")
    if stored is True and not derived:
        # Would only fire for a hypothetical future experiment whose
        # target_s is not 1; warn loudly rather than silently disagree.
        raise RuntimeError(
            f"hypothesisCorrect=True but hypothesisS={hyp_s} != "
            f"{SOUNDNESS_TARGET_S}; the plot script's hardcoded target_s "
            "is stale.  Update SOUNDNESS_TARGET_S or read target_s from the trial."
        )
    return derived


def _classify_trial(trial: dict) -> str:
    """Return the 4-way mechanism bucket for a trial."""
    outcome = trial.get("outcome", "")
    if outcome == "accept":
        return "accept_correct" if _is_correct(trial) else "accept_wrong"
    if outcome == "reject_insufficient_weight":
        return "reject_insufficient_weight"
    if outcome == "reject_list_too_large":
        return "reject_list_too_large"
    # Defensive default: unknown outcomes should not appear but if one ever
    # does we bin it with the weight-reject category so it doesn't silently
    # disappear from the plot.
    return "reject_insufficient_weight"


def build_tables(
    trials: list[dict],
) -> tuple[
    dict[tuple[str, int], float],          # rejection_rate[(strategy, n)]
    dict[tuple[str, int], tuple[float, float]],  # ci[(strategy, n)]
    dict[tuple[str, int, str], int],       # mechanisms[(strategy, n, bucket)]
    dict[tuple[str, int], float],          # bad_accept_rate[(strategy, n)]
    dict[tuple[str, int], tuple[float, float]],  # bad_accept_ci[(strategy, n)]
    list[int],                              # sorted n values
]:
    """Aggregate trial-level data into per-(strategy, n) summaries.

    Audit fix m1 (audit/soundness.md): the operationally relevant
    soundness event from Definition 7 / Eq. (18) is
    ``Pr[accepted AND not hypothesis_correct]``, not
    ``Pr[accepted]``.  For the ``random_list`` strategy, the verifier
    is *allowed* to accept when the random list happens to contain the
    true parity (the output hypothesis is then correct, so the event
    that Eq. (18) bounds does not occur).  Reporting ``rejection_rate``
    alone conflates these lucky correct accepts with potential
    soundness violations and falsely makes the dip below
    :math:`1 - \\delta` at small ``n`` look like a violation.  This
    function therefore returns:

    * ``rejection_rate`` and ``ci`` — the raw ``Pr[reject]`` retained for
      comparison with prior versions of the figure and for the CSV.
    * ``bad_accept_rate`` and ``bad_accept_ci`` — ``Pr[accept AND wrong]``,
      the quantity Eq. (18) bounds.
    * ``mechanisms`` — a 4-way decomposition keyed by
      ``(strategy, n, bucket)`` for ``bucket`` in :data:`MECHANISM_KEYS`.
      The ``accept`` category is split into ``accept_correct`` and
      ``accept_wrong`` so the stacked-bar plots can colour lucky accepts
      differently from Eq. (18) violations.
    """
    counts: dict[tuple[str, int], int] = defaultdict(int)
    rejects: dict[tuple[str, int], int] = defaultdict(int)
    bad_accepts: dict[tuple[str, int], int] = defaultdict(int)
    mechanisms: dict[tuple[str, int, str], int] = defaultdict(int)

    for t in trials:
        strategy = t["phiDescription"].replace("soundness_", "")
        n = t["n"]
        counts[(strategy, n)] += 1
        accepted = bool(t.get("accepted", False))
        if not accepted:
            rejects[(strategy, n)] += 1
        # Eq. (18) bad-accept event.  Use :func:`_is_correct` so the check
        # is robust to the pre-audit ``.pb`` file whose ``hypothesisCorrect``
        # field is not populated for dishonest trials.
        if accepted and not _is_correct(t):
            bad_accepts[(strategy, n)] += 1
        mechanisms[(strategy, n, _classify_trial(t))] += 1

    rejection_rate: dict[tuple[str, int], float] = {}
    ci: dict[tuple[str, int], tuple[float, float]] = {}
    bad_accept_rate: dict[tuple[str, int], float] = {}
    bad_accept_ci: dict[tuple[str, int], tuple[float, float]] = {}
    for key, total in counts.items():
        rej = rejects.get(key, 0)
        rejection_rate[key] = rej / total if total else 0
        ci[key] = wilson_ci(rej, total)
        bad = bad_accepts.get(key, 0)
        bad_accept_rate[key] = bad / total if total else 0
        bad_accept_ci[key] = wilson_ci(bad, total)

    ns = sorted({n for (_, n) in counts})
    return rejection_rate, ci, mechanisms, bad_accept_rate, bad_accept_ci, ns


