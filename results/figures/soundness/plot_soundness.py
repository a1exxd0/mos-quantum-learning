def plot_rejection_by_strategy(
    rejection_rate: dict[tuple[str, int], float],
    ci: dict[tuple[str, int], tuple[float, float]],
    bad_accept_rate: dict[tuple[str, int], float],
    bad_accept_ci: dict[tuple[str, int], tuple[float, float]],
    ns: list[int],
) -> None:
    """Two-panel grouped bar chart for the soundness figure.

    Left panel (the operationally relevant one): the **Definition 7 /
    Eq. (18) soundness indicator**

        soundness_indicator = 1 - Pr[accepted AND wrong hypothesis]
                            = Pr[rejected OR (accepted AND correct)].

    Eq. (18) bounds ``Pr[accepted AND wrong] <= delta = 0.1``, so the
    soundness indicator must sit at or above ``1 - delta = 0.9``.  The
    grey dashed line marks this threshold.  With the current data every
    bar sits at exactly 1.00 in every (strategy, n) cell — the empirical
    bad-accept count is 0/100 everywhere — so the dip below 0.9 that the
    previous (raw-rejection) figure showed at random_list, n=4 is an
    artefact of plotting the wrong quantity.

    Right panel (for context): the raw rejection rate used by the
    pre-fix figure.  The random_list cells at small n visibly dip below
    the 1 - delta line because the verifier *correctly* accepts the
    fraction ~ 5/2^n of trials where the random list happens to contain
    the true parity — these are lucky accepts with the correct
    hypothesis, not soundness violations, which is exactly why the
    left panel is the right way to visualise the theorem's promise.
    """
    if len(ns) <= 6:
        ns_show = ns
    else:
        ns_show = ns[::4]  # every 4th
        if ns[-1] not in ns_show:
            ns_show.append(ns[-1])

    fig, axes = plt.subplots(1, 2, figsize=(13, 4.4), sharey=True)
    x = np.arange(len(STRATEGY_ORDER))
    width = 0.8 / len(ns_show)
    colours = sns.color_palette("colorblind", len(ns_show))

    # ---- Left: Definition 7 soundness indicator ------------------------
    ax_left = axes[0]
    for i, n in enumerate(ns_show):
        rates = [1.0 - bad_accept_rate.get((s, n), 0.0) for s in STRATEGY_ORDER]
        # Wilson CI on 1 - bad_accept is just the mirror of the CI on bad_accept.
        lo = [1.0 - bad_accept_ci.get((s, n), (0.0, 0.0))[1] for s in STRATEGY_ORDER]
        hi = [1.0 - bad_accept_ci.get((s, n), (0.0, 0.0))[0] for s in STRATEGY_ORDER]
        yerr_lo = [max(0.0, r - l) for r, l in zip(rates, lo)]
        yerr_hi = [max(0.0, h - r) for r, h in zip(rates, hi)]
        offset = (i - len(ns_show) / 2 + 0.5) * width
        ax_left.bar(
            x + offset,
            rates,
            width * 0.9,
            label=f"$n={n}$",
            color=colours[i],
            yerr=[yerr_lo, yerr_hi],
            capsize=2,
            edgecolor="white",
            linewidth=0.5,
        )
    ax_left.set_xticks(x)
    ax_left.set_xticklabels([STRATEGY_LABELS[s] for s in STRATEGY_ORDER])
    ax_left.set_ylabel(
        r"Def 7 indicator"
        "\n"
        r"$1 - \Pr[\mathrm{accept} \wedge \mathrm{wrong}]$"
    )
    ax_left.set_ylim(0, 1.08)
    ax_left.axhline(0.9, ls="--", color="grey", alpha=0.7,
                    label=r"$1 - \delta = 0.9$ (Eq. 18)")
    ax_left.legend(fontsize=7, ncol=3, loc="lower right", framealpha=0.9)
    ax_left.set_title(
        "Definition 7 soundness indicator"
        "\n"
        r"$\Pr[\mathrm{reject} \vee (\mathrm{accept} \wedge \mathrm{correct})]$"
    )

    # ---- Right: raw rejection rate (for context) -----------------------
    ax_right = axes[1]
    for i, n in enumerate(ns_show):
        rates = [rejection_rate.get((s, n), 0.0) for s in STRATEGY_ORDER]
        lo = [ci.get((s, n), (0.0, 0.0))[0] for s in STRATEGY_ORDER]
        hi = [ci.get((s, n), (0.0, 0.0))[1] for s in STRATEGY_ORDER]
        yerr_lo = [max(0.0, r - l) for r, l in zip(rates, lo)]
        yerr_hi = [max(0.0, h - r) for r, h in zip(rates, hi)]
        offset = (i - len(ns_show) / 2 + 0.5) * width
        ax_right.bar(
            x + offset,
            rates,
            width * 0.9,
            color=colours[i],
            yerr=[yerr_lo, yerr_hi],
            capsize=2,
            edgecolor="white",
            linewidth=0.5,
        )
    ax_right.set_xticks(x)
    ax_right.set_xticklabels([STRATEGY_LABELS[s] for s in STRATEGY_ORDER])
    ax_right.set_ylabel(r"Raw rejection rate $\Pr[\mathrm{reject}]$")
    ax_right.axhline(0.9, ls="--", color="grey", alpha=0.7)
    ax_right.set_title(
        "Raw rejection rate (context)"
        "\n"
        r"dips below $1-\delta$ at random_list, small $n$ are lucky correct accepts"
    )

    fig.suptitle(
        "Soundness: Definition 7 / Eq. (18) indicator vs raw rejection rate",
        fontsize=12,
        y=1.04,
    )
    fig.tight_layout()
    save(fig, "rejection_by_strategy")


