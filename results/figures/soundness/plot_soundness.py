def plot_rejection_mechanism(
    mechanisms: dict[tuple[str, int, str], int],
    ns: list[int],
) -> None:
    """Stacked bar chart: per-strategy mechanism breakdown, pooled across n.

    Audit fix m1: the ``accept`` category is split into ``accept_correct``
    (lucky accepts with the correct parity — *not* an Eq. (18) event) and
    ``accept_wrong`` (the actual bad-accept event bounded by Eq. (18)).  The
    ``reject_list_too_large`` category is auto-hidden when no trials hit it
    (which is always the case at the current parameters: the maximum
    adversarial list size is 10 and the enforced bound is 712).
    """
    fig, ax = plt.subplots(figsize=(7, 4.2))

    # Aggregate across all n for each strategy
    strategy_totals: dict[str, int] = defaultdict(int)
    strategy_list_rej: dict[str, int] = defaultdict(int)
    strategy_weight_rej: dict[str, int] = defaultdict(int)
    strategy_accept_correct: dict[str, int] = defaultdict(int)
    strategy_accept_wrong: dict[str, int] = defaultdict(int)

    for (strategy, _n, bucket), count in mechanisms.items():
        strategy_totals[strategy] += count
        if bucket == "reject_list_too_large":
            strategy_list_rej[strategy] += count
        elif bucket == "reject_insufficient_weight":
            strategy_weight_rej[strategy] += count
        elif bucket == "accept_correct":
            strategy_accept_correct[strategy] += count
        elif bucket == "accept_wrong":
            strategy_accept_wrong[strategy] += count

    x = np.arange(len(STRATEGY_ORDER))
    totals = [strategy_totals[s] for s in STRATEGY_ORDER]

    weight_frac = [strategy_weight_rej[s] / totals[i]
                   for i, s in enumerate(STRATEGY_ORDER)]
    list_frac = [strategy_list_rej[s] / totals[i]
                 for i, s in enumerate(STRATEGY_ORDER)]
    acc_correct_frac = [strategy_accept_correct[s] / totals[i]
                        for i, s in enumerate(STRATEGY_ORDER)]
    acc_wrong_frac = [strategy_accept_wrong[s] / totals[i]
                      for i, s in enumerate(STRATEGY_ORDER)]

    # Colour palette: weight-reject, list-reject, accept-correct, accept-wrong.
    # The bad-accept colour is deliberately red to stand out as the one bucket
    # bounded by Eq. (18).
    palette = sns.color_palette("colorblind", 6)
    col_weight = palette[0]          # blue
    col_list = palette[1]            # orange
    col_accept_ok = palette[2]       # green
    col_accept_bad = "#c1272d"       # red (distinct from palette so it reads
                                     #      as the Eq. (18) hazard band)

    bottoms = np.zeros(len(STRATEGY_ORDER))
    ax.bar(x, weight_frac, bottom=bottoms,
           label="Reject: insufficient weight",
           color=col_weight, edgecolor="white", linewidth=0.5)
    bottoms = bottoms + np.array(weight_frac)

    # Auto-hide the list-too-large band when no trials hit it.
    list_total = sum(list_frac)
    if list_total > 0:
        ax.bar(x, list_frac, bottom=bottoms,
               label="Reject: list too large",
               color=col_list, edgecolor="white", linewidth=0.5)
        bottoms = bottoms + np.array(list_frac)

    ax.bar(x, acc_correct_frac, bottom=bottoms,
           label="Accept (correct hypothesis)",
           color=col_accept_ok, edgecolor="white", linewidth=0.5)
    bottoms = bottoms + np.array(acc_correct_frac)

    # Only show the bad-accept band in the legend if any trial actually hit
    # it.  At the current parameters it is empty in every cell — the whole
    # point of the fix is to make that absence visible.
    bad_total = sum(acc_wrong_frac)
    if bad_total > 0:
        ax.bar(x, acc_wrong_frac, bottom=bottoms,
               label=r"Accept $\wedge$ wrong (Eq. 18 event)",
               color=col_accept_bad, edgecolor="white", linewidth=0.5)
    else:
        # Still draw a (zero-height) entry so the legend includes the Eq. (18)
        # band; readers should see that this category exists but is empty.
        ax.bar(x, acc_wrong_frac, bottom=bottoms,
               label=r"Accept $\wedge$ wrong (Eq. 18 event; 0/6800)",
               color=col_accept_bad, edgecolor="white", linewidth=0.5)

    ax.set_xticks(x)
    ax.set_xticklabels([STRATEGY_LABELS[s] for s in STRATEGY_ORDER])
    ax.set_ylabel("Proportion of trials")
    ax.set_ylim(0, 1.08)
    ax.legend(fontsize=7, loc="lower right", framealpha=0.9)
    ax.set_title(
        "Soundness: mechanism breakdown (all $n$ pooled)"
        "\n"
        r"Accept band split by $\mathrm{hypothesis\_correct}$;"
        r" bad-accept is the Eq. (18) event"
    )

    # Annotate percentages in the centre of each bar.  Show two numbers:
    # total rejected, and lucky accepts (accept_correct), which is the
    # quantity that made the old figure misleading at random_list.
    for i, s in enumerate(STRATEGY_ORDER):
        rej_pct = (weight_frac[i] + list_frac[i]) * 100
        lucky_pct = acc_correct_frac[i] * 100
        if lucky_pct > 0.5:
            label = f"{rej_pct:.1f}% rejected\n{lucky_pct:.1f}% lucky accept"
        else:
            label = f"{rej_pct:.1f}% rejected"
        ax.text(i, 0.5, label, ha="center", va="center",
                fontsize=7.5, fontweight="bold")

    save(fig, "rejection_mechanism")


